#Bucle while: Escribe un programa en Python que utilice 
# un bucle while para imprimir los primeros 5 números naturales.

# Solicitar al usuario ingresar un dato
usuario_input = input("Por favor, ingresa un dato: ")

# Imprimir los primeros 5 números naturales usando un bucle while
contador = 1
while contador <= 5:
    print(contador)
    contador += 1

# Imprimir el dato ingresado por el usuario
print("Dato ingresado por el usuario:", usuario_input)


